    public Connection checkConnection(){
      Connection kon = null;
       try{
           RegistryKey regist = new RegistryKey();
           String database = textDatabase.getText();
           String username = textUsername.getText();
           String password = new String(textPassword.getPassword());
           String host = textHost.getText();
           Class.forName("org.gjt.mm.mysql.Driver");
           kon = DriverManager.getConnection("jdbc:mysql://"+host+"/"+database+"", username, password);
           return kon;
       }catch(SQLException sqlEx){
           System.out.println("Database tidak bisa dibuka");
           return null;
       }catch(Exception ex){
           System.out.println("Anda tidak terhubung ke database");
           return null;
       }   
    }
    
    public void updateDatabase(String host, String database, String username, String password){
        RegistryKey regist = new RegistryKey();
        regist.setKeyString("database", database);
        regist.setKeyString("username", username);
        regist.setKeyString("password", password);
        regist.setKeyString("host", host);
    }

