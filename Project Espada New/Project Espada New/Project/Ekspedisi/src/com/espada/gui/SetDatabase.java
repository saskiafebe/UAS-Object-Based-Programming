    private void btnCheckConnectionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckConnectionActionPerformed
        // TODO add your handling code here:
        if(this.checkConnection()!=null){
            this.statusTempConnection=true;
            JOptionPane.showMessageDialog(this, "Koneksi berhasil");
        }else{
            this.statusTempConnection=false;
            JOptionPane.showMessageDialog(this, "Koneksi tidak berhasil");
        }
        
        if(this.statusTempConnection==true){
            btnApply.setEnabled(true);
        }else{
            btnApply.setEnabled(false);
        }
    }//GEN-LAST:event_btnCheckConnectionActionPerformed

